    private String Struk(String metode) {
        String s = "          STARCHY FOOD & DRINK          \n";
               s += "========================================\n";
        int total = 0;
        for (org.itenas.is.oop.starchy.models.TransaksiDetail d : listBelanja) {
            String nama = d.getMenu().getMenu_nama();
            int qty = d.getJumlah();
            int sub = d.getSubtotal();
            int hrg = sub / qty;
            
            s += nama + "\n";
            s += "  " + qty + " x " + hrg + " = " + sub + "\n";
            total += sub;
        }
        s += "----------------------------------------\n";
        s += "TOTAL  : Rp " + total + "\n";
        s += "METODE : " + metode + "\n";
        s += "========================================\n";
        s += "              TERIMAKAISH                 ";
        return s;
    }
    
    private void cetakKePDF(String teks) {
        PrinterJob job = PrinterJob.getPrinterJob();
        job.setPrintable((Graphics g, PageFormat pf, int pageIndex) -> {
            if (pageIndex > 0) return Printable.NO_SUCH_PAGE;

            Graphics2D g2d = (Graphics2D) g;
            g2d.translate(pf.getImageableX(), pf.getImageableY());
            g.setFont(new Font("Monospaced", Font.PLAIN, 10)); // Font struk biar rapi

            int y = 20;
            for (String line : teks.split("\n")) {
                g.drawString(line, 10, y);
                y += 15;
            }
            return Printable.PAGE_EXISTS;
        });

        if (job.printDialog()) { // Munculin dialog print
            try { job.print(); } catch (PrinterException e) { e.printStackTrace(); }
        }
    }
