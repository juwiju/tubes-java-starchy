    private void btnSimpanEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanEditActionPerformed
        // TODO add your handling code here:
        try {
            int baris = tblTransaksi.getSelectedRow();

            if (baris != -1) {
                // Ambil semua inputan baru
                String idBaru = txtMenuID.getText();
                int qtyBaru = Integer.parseInt(txtJumlah.getText());
                String metBaru = cbMetode.getSelectedItem().toString();

                ctrlDetail.update(listBelanja, baris, idBaru, qtyBaru, metBaru);

                int hargaBaru = listBelanja.get(baris).getMenu().getMenu_harga();
                int subtotalBaru = listBelanja.get(baris).getSubtotal();
            
                // Update baris di tabel 
                tableModel.setValueAt(idBaru, baris, 0);
                tableModel.setValueAt(qtyBaru, baris, 1);
                tableModel.setValueAt(hargaBaru, baris, 2);
                tableModel.setValueAt(metBaru, baris, 3);
                tableModel.setValueAt(subtotalBaru, baris, 4);

                // Reset agar dapat input seperi biasa
                this.isModeEdit = false;
                clearFields();
                JOptionPane.showMessageDialog(this, "Berhasil mengupdate!");

            } else {
                JOptionPane.showMessageDialog(this, "Pilih barang yang ingin diupdate!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Input bermasalah: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanEditActionPerformed

