    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
      if (txtKode.getText().isEmpty() ||
        txtNama.getText().isEmpty() ||
        txtHarga.getText().isEmpty() ||
        txtStok.getText().isEmpty()) {

        JOptionPane.showMessageDialog(this, "Data Belum Diisi!");
        return;
    }

    MenuSingle menu = new MenuSingle(
        Integer.parseInt(txtStok.getText()),
        txtKode.getText(),
        txtNama.getText(),
        Integer.parseInt(txtHarga.getText())
    
    );

    controller.create(menu); 
        loadTable();
        clearForm();
    
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
       if (txtNama.getText().isEmpty() ||
        txtHarga.getText().isEmpty() ||
        txtStok.getText().isEmpty()) {

        JOptionPane.showMessageDialog(this, "TIdak Ada Data Yang Diubah!");
        return;
    }

    MenuSingle menu = new MenuSingle(
        Integer.parseInt(txtStok.getText()),
        txtKode.getText(),
        txtNama.getText(),
        Integer.parseInt(txtHarga.getText())
    );

    controller.update(txtKode.getText(), menu);
    loadTable();
    clearForm();
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
          controller.delete(txtKode.getText());
    loadTable();
    clearForm();
    }//GEN-LAST:event_btnHapusActionPerformed

    private void tblMenuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMenuMouseClicked
       int row = tblMenu.getSelectedRow();

     txtKode.setText(tblMenu.getValueAt(row, 0).toString());
     txtNama.setText(tblMenu.getValueAt(row, 1).toString());
     txtHarga.setText(tblMenu.getValueAt(row, 2).toString());
     txtStok.setText(tblMenu.getValueAt(row, 3).toString());
     txtKode.setEditable(false);
    }//GEN-LAST:event_tblMenuMouseClicked

