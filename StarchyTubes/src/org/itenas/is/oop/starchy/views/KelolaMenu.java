    private void loadTable() {
    DefaultTableModel model = (DefaultTableModel) tblMenu.getModel();
    model.setRowCount(0);

    List<MenuSingle> list = controller.show();
    for (MenuSingle m : list) {
        model.addRow(new Object[]{
            m.getMenu_id(),
            m.getMenu_nama(),
            m.getMenu_harga(),
            m.getStok()
        });
    }
}
